namespace MauticPlugin\MauticVtigerCrmBundle\Vtiger\Model;

use MauticPlugin\MauticVtigerCrmBundle\Vtiger\Type\CommonType;
use MauticPlugin\MauticVtigerCrmBundle\Vtiger\Type\TypeFactory;

class ModuleFieldInfo
{
    /**
     * @var string
     */
    private $name;

    /**
     * @var string
     */
    private $label;

    /**
     * @var bool
     */
    private $mandatory;

    /**
     * @var CommonType
     */
    private $type;

    /**
     * @var bool
     */
    private $isUnique;

    /**
     * @var bool
     */
    private $nullable;

    /**
     * @var bool
     */
    private $editable;

    /**
     * @var string
     */
    private $default;

    /**
     * ModuleFieldInfo constructor.
     *
     * @param \stdClass $data
     *
     * @throws \Exception
     */
    public function __construct(\stdClass $data)
    {
        $this->label    = $data->label;
        $this->name     = $data->name;
        $this->nullable = $data->nullable;
        $this->editable = $data->editable;

        $this->setDefault($data);
        $this->setIsUnique($data);
        $this->setMandatory($data->mandatory, $this->name);

        $this->type = TypeFactory::create($data->type);
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @return string
     */
    public function getLabel(): string
    {
        return $this->label;
    }

    /**
     * @return bool
     */
    public function isMandatory(): bool
    {
        return $this->mandatory;
    }

    /**
     * @return mixed
     */
    public function getTypeName()
    {
        return $this->getType()->getName();
    }

    /**
     * @return CommonType
     */
    public function getType()
    : CommonType
    {
        return $this->type;
    }

    /**
     * @return bool
     */
    public function isUnique(): bool
    {
        return $this->isUnique;
    }

    /**
     * @return bool
     */
    public function isNullable(): bool
    {
        return $this->nullable;
    }

    /**
     * @return bool
     */
    public function isEditable(): bool
    {
        return $this->editable;
    }

    /**
     * @return string
     */
    public function getDefault(): string
    {
        return $this->default;
    }

    /**
     * @param \stdClass $data
     */
    private function setDefault(\stdClass $data): void
    {
        $this->default = $data->default ?? null;
    }

    /**
     * @param \stdClass $data
     */
    private function setIsUnique(\stdClass $data): void
    {
        if (!isset($data->isunique) && in_array('autogenerated', (array) $data->type, true)) {
            $this->isUnique = true;
        } elseif (!isset($data->isunique)) {
            $this->isUnique = false;
        } else {
            $this->isUnique = $data->isunique;
        }
    }

    /**
     * @param bool   $mandatory
     * @param string $name
     */
    private function setMandatory(bool $mandatory, string $name): void
    {
        $this->mandatory = $mandatory || 'email' === $name;
    }
}
